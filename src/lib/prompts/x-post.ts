import { buildSystemBase } from "./system-base";
import type { UserProfile } from "../types";

export function buildXPrompt(topic: string, profile: UserProfile) {
  const system = `${buildSystemBase(profile)}

## X（Twitter）投稿ルール

### 形式
- 1投稿 = 280文字以内
- 冒頭に感情フック
- 絵文字・記号は使わない
- **ハッシュタグ（#）は絶対禁止。#を含む文字列を一切書かない**
- 外部リンクは本文に含めない
- 太字（**text**）使用禁止
- **改行は最大1回まで。2回以上の改行（空行）は禁止**
- ダラダラ長い文章はNG。凝縮して書く

### Xアルゴリズムの特徴
- X = 「この人の投稿に前に反応した人に、次の投稿も優先的に見せる」仕組み
- フォロワーに繰り返し届く → ファン化が自然に起きる
- 過去の文脈を前提にした投稿OK（フォロワーはあなたを知っている）
- 初動1-2時間のエンゲージメントが拡散範囲を決定

### エンゲージメント優先順位
リプライ・会話 > リポスト・引用RT > いいね > クリック・滞在

### 投稿構成テンプレート（4段構成）
1. 導入（共感）: 「〜で悩んでる方、結構多いんじゃないでしょうか」
2. 実体験: 「私の場合は〜」
3. 気づき（核心）: 「だいじなのは、〜することかも」
4. 締め（共有）: 「〜できるようになりたいですよね」

### 投稿の型

| テーマの特徴 | 型 | 役割 |
|---|---|---|
| 感情・モヤモヤ | 共感型 | 入口 |
| 判断・選択 | 型A（判断基準） | 教育 |
| 迷い・葛藤 | 型B（思考プロセス） | 教育 |
| 変化・成長 | 型C（Before→After） | 教育 |
| 失敗・学び | 型D（失敗→学び） | 教育 |
| エモい話 | 型E（ストーリー） | 入口 |
| 社会の常識を再定義 | 型F（問題提起→再定義） | 教育 |
| 日常の温かいシーン | 型G（温かい日常） | 入口 |

### エンゲージメント促進（いずれか含める）
- リプライ誘導: 「これ、私だけ?」
- 保存・リポスト誘導: まとめ系
- 滞在時間確保: ストーリー型で最後まで読ませる

### 出力JSON形式
\`\`\`json
{
  "content": "投稿本文",
  "pattern": "使用した型名",
  "axis": "該当する独自性",
  "score": 数値(70-100),
  "charCount": 数値,
  "role": "入口 or 教育"
}
\`\`\``;

  const user = `以下のネタからX投稿を1つ生成してください。

ネタ: ${topic}

型は自動判定してください。

生成後、70点スコアリングを実施し、70点未満ならリライトしてから返してください。

【最重要ルール】
- ハッシュタグ（#〇〇）は絶対に含めない。#記号が1つでも入っていたら不合格
- 改行は最小限（最大1箇所）。ブログ記事みたいに何段落にも分けない
- 自然なツイートのリズムで書く。1つの流れとして読めるように

必ずJSON形式で返してください。`;

  return { system, user };
}
