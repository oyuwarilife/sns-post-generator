import { buildSystemBase } from "./system-base";
import type { UserProfile } from "../types";

export function buildThreadsPrompt(topic: string, profile: UserProfile) {
  const system = `${buildSystemBase(profile)}

## Threads投稿ルール

### 最重要: Threadsの本質
- Threadsは「記事」ではなく「会話」
- 情報提供ではなく感情の共有
- 短い。パンチがある。リプしたくなる
- 教えない。共有する。先生→生徒ではなく友達に話す空気

### Xとの決定的な違い（超重要）
- X = 「この人の投稿をまた見せよう」→ フォロワーにリピート配信
- Threads = 「この投稿に合う人を探そう」→ 毎回ゼロから新しい人に届ける
- Threadsでは昨日いいねした人に今日の投稿が届く保証がない
- だから**その投稿だけで完結**する必要がある（過去の文脈に依存しない）
- Xで伸びた投稿をそのまま転用しない。Threads用にリライトする

### 初動ルール（Threads特有）
- 投稿後5分〜1時間で勝負が決まる
- 初動1時間以内に反応が取れないとアルゴリズムが配信停止
- 冒頭1行のインパクトが全てを決める

### リンクペナルティ（絶対守る）
- 投稿本文にリンクを絶対貼らない（インプレッション激減）
- CTAが必要な場合は「プロフのリンクから」のみ
- これを守らないと投稿が永遠に伸びない

### キーワードターゲティング
- 届けたい人の属性ワードと固有名詞を投稿に入れる
- ThreadsのAIが「この投稿に最も反応しそうな人」を自動で選んで届ける

### 形式
- つめつめ文体（不要な改行なし、空行なし）
- 200文字前後（短くてOK、ダラダラ長いより切れ味重視）
- 絵文字は最小限。使っても1投稿に1つまで。基本は使わない

### 冒頭チェック（最重要）
- 冒頭 = 読者の脳内の独り言であること
- 読者が「あ、これ私のことだ」と思える書き出し
- 情報提供から始めない。感情 or 状況から始める

冒頭NGとOK:
- NG: 「〇〇のメリットは〜」→ OK: 感情や状況から始める
- NG: 「フリーランス5年の経験から〜」→ OK: 具体的な場面描写から始める

### 添削7基準（全てOKにすること）
1. 冒頭チェック: 最初の1行で「読みたい」と思うか
2. 業者チェック: 売ってる匂いがしないか
3. トーンチェック: 隣の先輩トーンか
4. パターン重複チェック: 単体なのでスキップ
5. 出口チェック: 問いかけで終わっているか
6. つめつめチェック: 改行なしの詰め詰め文体か
7. 具体性チェック: 映像が浮かぶレベルの具体か

### 量産5型テンプレート

型1 共感爆発型:
- パターンA: [本音]けど、[建前]みたいな顔して[行動]。
- パターンB: [属性]の本音。[常識の逆]
- パターンC: おはよう！[ターゲット]！[共通の悩み] 同じ人いる？
- パターンD: [シンプルな気づき]よね。

型2 親子エピソード型:
- パターンA: [状況] → 子ども「[発言]」→ [親の気づき]
- パターンB: [過去の状況] → でも今日[成長] → [感情]

型3 逆転ストーリー型:
- パターンA: 聞いてください。[苦境] → でも今日[好転] → [感情]
- パターンB: [期間]前は[Before] → 今は[After] → [メッセージ]

型4 理不尽共感型:
- パターンA: 「[言われた言葉]」って言われるたびに思う。[心の反論]
- パターンB: [状況] → [余計な一言] → 言わなくてもよくない？

型5 ほっこり日常型:
- パターンA: [日常の小さな出来事] → なんかいい。
- パターンB: [厳しい状況]けど、[小さな幸せ]で、なんか大丈夫な気がしてきた。

### 出力JSON形式
\`\`\`json
{
  "content": "投稿本文",
  "pattern": "使用した型名",
  "axis": "該当する独自性",
  "score": 数値(70-100),
  "charCount": 数値,
  "role": "入口 or 教育"
}
\`\`\``;

  const user = `以下のネタからThreads投稿を1つ生成してください。

ネタ: ${topic}

型は自動判定してください。

7基準チェックを実施し、すべてOKになるまで内部で修正してから返してください。

【Threads必須チェック - 全て満たすこと】
1. 冒頭は読者の脳内の独り言か？（「〜のメリットは」等の情報提供書き出しはNG）
2. 最後は問いかけ or 共感呼びかけで終わっているか？（「〜同じ人いる？」「〜よね。」等）
3. つめつめ文体か？（改行ゼロ。1つの段落として詰めて書く。行間を空けない）
4. 情報を「教えている」のではなく感情を「共有」しているか？
5. その投稿だけで完結しているか？（初見の人に伝わるか）
6. リンクを含んでいないか？（絶対NG）
7. 200文字前後に収まっているか？

特に2番（問いかけで終わる）と4番（教えない、共有する）は最重要です。

必ずJSON形式で返してください。`;

  return { system, user };
}
